declare(strict_types=1);

namespace Magento\CustomerStorefrontGraphQl\Model\Resolver\Customer;

use Magento\Framework\GraphQl\Config\Element\Field;
use Magento\Framework\GraphQl\Query\ResolverInterface;
use Magento\Framework\GraphQl\Schema\Type\ResolveInfo;
use Magento\Framework\Exception\LocalizedException;

/**
 * Resolve Customer.is_subscribed field
 */
class IsSubscribed implements ResolverInterface
{
    /**
     * @inheritdoc
     */
    public function resolve(
        Field $field,
        $context,
        ResolveInfo $info,
        array $value = null,
        array $args = null
    ) {
        if (!isset($value['model'])) {
            throw new LocalizedException(__('"model" value should be specified'));
        }
        $customer = $value['model'];
        $customerExtension = $customer->getExtensionAttributes();
        return $customerExtension['is_subscribed'];
    }
}
